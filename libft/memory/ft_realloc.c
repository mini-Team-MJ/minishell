#include "../libft.h"

void	*ft_realloc(void *ptr, size_t old_size, size_t new_size)
{
	void	*new_ptr;
	size_t	to_copy;

	if (new_size == 0)
	{
		free(ptr);
		return (NULL);
	}
	new_ptr = malloc(new_size);
	if (!new_ptr)
		return (NULL);
	if (ptr)
	{
		if (old_size < new_size)
			to_copy = old_size;
		else
			to_copy = new_size;
		memcpy(new_ptr, ptr, to_copy);
		free(ptr);
	}
	return (new_ptr);
}
